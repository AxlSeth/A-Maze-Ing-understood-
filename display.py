def display_maze(maze: list[list[maze_gen.Cell]]) -> None:                     
    height: int = len(maze)
    width: int = len(maze[0])

    for y in range(height):
        for x in range(width):
            if x == 0:
                print(" " * vars.START_POINT, end="")
            print("‚Ėą‚Ėą", end="")
            if maze[y][x].walls["n"] == 1:
                print("‚Ėą‚Ėą", end="")
            else:
                print("  ", end="")
        print("‚Ėą‚Ėą")

        for x in range(width):
            if x == 0:
                print(" " * vars.START_POINT, end="")
            if maze[y][x].walls["w"] == 1:
                if maze[y][x].is_pattern == 1:
                    print("‚Ėą‚Ėą\033[31m##\033[0m", end="")
                else:
                    print("‚Ėą‚Ėą  ", end="")
            else:
                print("    ", end="")
        print("‚Ėą‚Ėą")

    for x in range(width):
        if x == 0:
            print(" " * vars.START_POINT, end="")
        print("‚Ėą‚Ėą", end="")
        if maze[height - 1][x].walls["s"] == 1:
            print("‚Ėą‚Ėą", end="")
        else:
            print("    ", end="")
    print("‚Ėą‚Ėą")


